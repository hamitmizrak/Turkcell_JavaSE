package com.hamitmizrak;

public class _03_PrimitiveTypes {
	
	public static void main(String[] args) {
		
		// primitive types: stack memory: hafıza uzayı belli olan,heap'e göre daha
		// hızlıdır deneme44
		
		// tam sayılar
		byte b1 = 44;
		short s1 = 129;
		int i1 = 444;
		long l1 = 1L + 2L;
		
		// Virgüllü sayılar
		float f2 = 44.55f;
		double d2 = 44.56;
		
		boolean b3 = 3 > 1;
		char c3 = '\u1252';
		System.out.println(c3);
		
		// bilimsel
		double bilimsel = 2E+3;
		System.out.println(bilimsel);
		
		int cellPhone1 = 1112233;
		System.out.println(cellPhone1);
		
		int cellPhone2 = 111_22_33;
		
		System.out.println(cellPhone2);
		
	}
	
}
